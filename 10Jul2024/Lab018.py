#String Slicing
str = "I am Learning python in July"

# print string from 0 to 4th
print(str[0:4])
# print string from 5 to 20th
print(str[5:20])
# print string from 8 to end of string
print(str[8:len(str)])
print(str[8:28])


print(str[0:4])
# start , stop , skip 
print(str[0:4:2])
print(str[0:12:2])
